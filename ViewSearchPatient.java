     void loadSearchData(){
        
        Object data [][]= new Object[patientname.length][7];
        for(int i = 0; i< data.length; i++){
            data[i] = new Object[]{patientname[i].getPatientId(),patientname[i].getSurname(),patientname[i].getFirstname(),patientname[i].getOthername(),patientname[i].getGender(),patientname[i].getPhonenumber(),patientname[i].getAddress()};
        String columns[] = {"PatientID", "Surname", "Firstname", "Othername", "Gender", "Phonenumber", "Address"};
        tableSearch.setModel(new DefaultTableModel(data, columns));
        totalFound.setText("Total Records: "+data.length);
        }
     }

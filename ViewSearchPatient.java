        if(tableSearch.getSelectedRows().length == 1){
          Patient  pat = p.getPatient(tableSearch.getValueAt(tableSearch.getSelectedRow(), 0).toString());
           Image Img = new ImageIcon(pat.getPhoto()).getImage().getScaledInstance(searchPassport.getWidth(),searchPassport.getHeight(), Image.SCALE_DEFAULT);
            searchPassport.setIcon(new ImageIcon(Img));
         }

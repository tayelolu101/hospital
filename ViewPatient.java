    private void viewDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDeleteActionPerformed
          if (JOptionPane.showConfirmDialog(this, "Do you wish to delete the selected record(s)", "Delete Employee", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
             PatientDatabseManager manager = new PatientDatabseManager();
             for(int r : tblPatients.getSelectedRows()){
                 manager.delete(tblPatients.getValueAt(r, 0).toString());
           }
             loadData();
         }  
    }//GEN-LAST:event_viewDeleteActionPerformed


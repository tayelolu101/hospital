    void loadData(){
        Patient ps[] = new PatientDatabseManager().getAllPatient();
        Object data [][]= new Object[ps.length][7];
        for(int i = 0; i< data.length; i++){
            data[i] = new Object[]{ps[i].getPatientId(),ps[i].getSurname(),ps[i].getFirstname(),ps[i].getOthername(),ps[i].getGender(),ps[i].getPhonenumber(),ps[i].getAddress()};
        }
        String columns[] = {"PatientID", "Surname", "Firstname", "Othername", "Gender", "Phonenumber", "Address"};
        tblPatients.setModel(new DefaultTableModel(data, columns));
        txtTotalRecords.setText("Total Records: "+data.length);
         
   }


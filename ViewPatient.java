    private void tblPatientsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPatientsMouseClicked
         PatientDatabseManager p = new PatientDatabseManager();
        if(tblPatients.getSelectedRows().length == 1){
          Patient  pat = p.getPatient(tblPatients.getValueAt(tblPatients.getSelectedRow(), 0).toString());
           Image Img = new ImageIcon(pat.getPhoto()).getImage().getScaledInstance(txtPassport.getWidth(),txtPassport.getHeight(), Image.SCALE_DEFAULT);
            txtPassport.setIcon(new ImageIcon(Img));
         }
    }//GEN-LAST:event_tblPatientsMouseClicked


    private void txtBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBrowseActionPerformed
        SelectPassport sp = new SelectPassport(null, true);
        sp.setLocationRelativeTo(null);
        sp.setVisible(true);
        if(imageData != null){
            Image Img = new ImageIcon(imageData).getImage().getScaledInstance(txtPassport.getWidth(),txtPassport.getHeight(), Image.SCALE_DEFAULT);
            txtPassport.setIcon(new ImageIcon(Img));
        }
    }//GEN-LAST:event_txtBrowseActionPerformed


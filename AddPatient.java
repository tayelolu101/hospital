    private void txtSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSubmitActionPerformed
        Patient p = new Patient();
        p.setPatientId(txtPatientId.getText());
        p.setFirstname(txtFirstname.getText());
        p.setSurname(txtSurname.getText());
        p.setOthername(txtOthername.getText());
        p.setGender(cmbGender.getSelectedItem().toString());
        p.setPhonenumber(txtPhone.getText());
        p.setAddress(txtAddress.getText());
        if(imageData != null){
            p.setPhoto(imageData);
        }else{
            p.setPhoto(new byte[]{});
        }
        PatientDatabseManager manager = new PatientDatabseManager();
        if(manager.exists(p.getPatientId())){
               JOptionPane.showMessageDialog(null, "PatientID already exists.","Error", JOptionPane.ERROR_MESSAGE);
               return;
        }
       manager.add(p);
        dispose();
    }//GEN-LAST:event_txtSubmitActionPerformed

